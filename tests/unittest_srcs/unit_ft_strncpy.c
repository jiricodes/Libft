static void test_ft_strncpy_one(void *s1, size_t n, int *current, int total) {
	
	*current += 1;

	char *dst_ft = (char *)malloc(sizeof(char) * n);
	char *dst_org = (char *)malloc(sizeof(char) * n);
	char *ft_ret = ft_strncpy(dst_ft, s1, n);
	char *org_ret = strncpy(dst_org, s1, n);
	assert(!memcmp(org_ret, ft_ret, n));
	free(org_ret);
	free(ft_ret);
	STATUS(*current, total);
}


