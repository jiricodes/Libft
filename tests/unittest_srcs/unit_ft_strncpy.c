static void test_ft_strncpy_speed(size_t mem_size, size_t n, size_t n_iter)
{
	CATEGORY("ft_strncpy performance");
	printf("source memory size = %zu bytes\n", mem_size);
	printf("destination memory size = %zu bytes\n", n);
	printf("number of iterations = %zu\n", n_iter);

	size_t len = mem_size;
	size_t iter = n_iter;

	clock_t start = 0;

	// setup test string
	char *sample = "andjkasb azoxkczkcmzlxkczm zxcn iu bhq uaao bkjcbcz1534 z83x4zx8c4 c1zx184 asda123465";
	size_t sample_len = strlen(sample);

	char *s1 = (char *)malloc(sizeof(char) * len);

	size_t ttl_len = 0;
	while (ttl_len < len) {
		size_t cpy_len = sample_len;
		if (ttl_len + sample_len > len) {
			cpy_len = len - ttl_len;
		}
		memcpy(s1 + ttl_len, sample, cpy_len);
		ttl_len += cpy_len;
	}

	s1[len - 1] = 0;

	char *ret = (char *)malloc(sizeof(char) * n);

	for (int i = 0; i < iter + 25; i++) {
		if (i == 24)
			start = clock();
		ret = strncpy(ret, s1, n);
	}
	clock_t end = clock();
	double ft_time = (double)(end - start) / CLOCKS_PER_SEC;
	SPEEDRES_NAME(iter / ft_time, "strncpy");
	for (int i = 0; i < iter + 10; i++) {
		if (i == 9)
			start = clock();
		ret = ft_strncpy(ret, s1, n);
	}
	end = clock();
	double new_time = (double)(end - start) / CLOCKS_PER_SEC;
	SPEEDRES_NAME(iter / new_time, "ft_strncpy");
	SPEEDCMP((ft_time / new_time) * 100, "strncpy");
	free(s1);
	free(ret);
}
